package model;

import java.awt.Image;
import java.awt.image.BufferedImage;

public abstract class Troupe extends EntiteGraphique
{

	// ##############################################################################################
    // 								ATTRIBUTS
    // ##############################################################################################

	protected BufferedImage bimgCourante;
	protected BufferedImage[] bimgs = new BufferedImage[4];
	
	
    // ##############################################################################################
    //							   CONSTRUCTEURS
    // ##############################################################################################
	
	public Troupe(int posX, int posY) {
		super(posX, posY);
	}

	//##############################################################################################
	//						ACCESSEUR
	//##############################################################################################*/

	public Image getImage() {
		return bimgCourante.getScaledInstance(sizeX, sizeY, Image.SCALE_DEFAULT);
	}

	//##############################################################################################
	//								DESCRIPTEUR
	//##############################################################################################*/

	public String toString()
	{
		return "Pos soldat : " + posX + " | " + posY + "\n";
	}

	
	//##############################################################################################
	//								Type de Camps
	//##############################################################################################*/
	
	public enum Camps {
		ennemy,
		allie;
	}

}
